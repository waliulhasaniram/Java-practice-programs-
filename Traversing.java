
package start;

public class Traversing {
   

    public static void main(String[] args) {
    
        int a[] = {1,2,3,4,5,6};
        
        System.out.println("Traversing: ");
        
        for(int i=1; i<a.length; i++){
            System.out.print(" " + a[i]);
        }
    }
}
